DROP TABLE IF EXISTS startup;

CREATE TABLE startup (
    Org_ID varchar(10),
    Name varchar(25),
    Street varchar(25),
    City varchar(25),
    State varchar(2),
    Zip varchar(5),
    Phone varchar(10),
    Email varchar(50),
    Website varchar(69),
    Need1 varchar(50),
    Need2 varchar(50),
    Need3 varchar(50),
    Need4 varchar(50),
    Need5 varchar(50)
);

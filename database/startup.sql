DROP TABLE IF EXISTS startup;

CREATE TABLE startup (
    userName VARCHAR(25),
    password VARCHAR(25),
    orgName varchar(25),
    firstName varchar(25),
    lastName varchar(25),
    street varchar(25),
    city varchar(25),
    state varchar(2),
    zip varchar(5),
    phone varchar(10),
    email varchar(50),
    website varchar(69),
    need1 varchar(50),
    need2 varchar(50),
    need3 varchar(50),
    need4 varchar(50),
    need5 varchar(50)
);

DROP TABLE if EXISTS investor;

CREATE TABLE investor(userID VARCHAR(10),
 firstName VARCHAR(25), lastName VARCHAR(25),
  companyName VARCHAR(25), availableFunding VARCHAR(10),
   interest1 VARCHAR(20), interest2 VARCHAR(20), interest3 VARCHAR(20),
   avgInvestment VARCHAR(10), phone VARCHAR(20), email VARCHAR(50),
   twitter VARCHAR(25), instagram VARCHAR(50), facebook VARCHAR(50),
    linkedIn VARCHAR(50));
